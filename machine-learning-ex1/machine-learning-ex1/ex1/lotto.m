%% ======================= Part 2: Plotting =======================
fprintf('Plotting Data ...\n')
data = load('dataset.txt');



X = data(:, 1); y = data(:, 2);

%[X mu sigma] = featureNormalize(X);

m = length(y); % number of training examples

% Plot Data
